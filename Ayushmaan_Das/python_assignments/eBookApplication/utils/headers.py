def print_header(title):
    """Helper to print headers with decoration."""
    print("\n" + "=" * 50)
    print(f"{title.center(50)}")
    print("=" * 50)


def print_subheader(subtitle):
    """Helper to print subheaders."""
    print("\n" + "-" * 50)
    print(f"{subtitle.center(50)}")
    print("-" * 50)